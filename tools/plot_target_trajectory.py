#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Plot target trajectory from target_world_positions JSONL.")
    parser.add_argument(
        "--input",
        default="outputs/target_world_positions.jsonl",
        help="Input JSONL path generated by fusion_tracking.",
    )
    parser.add_argument(
        "--output",
        default="outputs/target_trajectory_enu.png",
        help="Output trajectory image path.",
    )
    parser.add_argument(
        "--field",
        choices=["target_world_enu_m", "target_lidar_m"],
        default="target_world_enu_m",
        help="Trajectory field to plot.",
    )
    parser.add_argument("--dpi", type=int, default=150, help="Output image DPI.")
    parser.add_argument("--point-size", type=float, default=12.0, help="Scatter point size.")
    return parser.parse_args()


def load_records(path: Path, field: str) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    if not path.exists():
        raise FileNotFoundError(f"Input file not found: {path}")

    rows: list[tuple[float, float, float]] = []
    for line in path.read_text(encoding="utf-8", errors="ignore").splitlines():
        line = line.strip()
        if not line:
            continue
        try:
            item = json.loads(line)
        except json.JSONDecodeError:
            continue

        ts = item.get("timestamp")
        vec = item.get(field)
        if ts is None or not isinstance(vec, list) or len(vec) < 2:
            continue
        try:
            t = float(ts)
            x = float(vec[0])
            y = float(vec[1])
        except (TypeError, ValueError):
            continue
        rows.append((t, x, y))

    if len(rows) < 2:
        raise ValueError(f"Not enough valid points in {path} for field={field}. valid={len(rows)}")

    rows.sort(key=lambda row: row[0])
    arr = np.asarray(rows, dtype=np.float64)
    return arr[:, 0], arr[:, 1], arr[:, 2]


def plot_trajectory(t: np.ndarray, x: np.ndarray, y: np.ndarray, out_path: Path, field: str, point_size: float, dpi: int) -> None:
    fig, ax = plt.subplots(figsize=(9, 7), dpi=max(int(dpi), 60))
    sc = ax.scatter(x, y, c=(t - t.min()), s=max(float(point_size), 1.0), cmap="viridis", alpha=0.9)
    ax.plot(x, y, color="#4c78a8", linewidth=1.0, alpha=0.55)
    ax.scatter([x[0]], [y[0]], color="#2ca02c", s=80, marker="o", label="start")
    ax.scatter([x[-1]], [y[-1]], color="#d62728", s=80, marker="X", label="end")

    title = "Target Trajectory"
    if field == "target_world_enu_m":
        title += " (ENU)"
        ax.set_xlabel("East (m)")
        ax.set_ylabel("North (m)")
    else:
        title += " (LiDAR XY)"
        ax.set_xlabel("X (m)")
        ax.set_ylabel("Y (m)")
    ax.set_title(title)
    ax.grid(True, alpha=0.3)
    ax.axis("equal")
    ax.legend(loc="best")
    cbar = fig.colorbar(sc, ax=ax, pad=0.02)
    cbar.set_label("Elapsed Time (s)")

    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path)
    plt.close(fig)


def main() -> None:
    args = parse_args()
    input_path = Path(str(args.input))
    output_path = Path(str(args.output))
    t, x, y = load_records(input_path, args.field)
    plot_trajectory(t, x, y, output_path, args.field, args.point_size, args.dpi)
    print(
        f"saved={output_path} points={len(t)} duration_s={float(t.max()-t.min()):.3f} field={args.field}"
    )


if __name__ == "__main__":
    main()
